# 나중에 그 이름으로 다시 쓸 수 있어. print()는 그 값을 화면에 찍는 함수야.
# f-string은 문자열 안에 변수 값을 끼워 넣을 때 쓰면 편해.
#
# [import]
# 이 파일은 별도 import 없이 파이썬 내장 함수만 사용 (print, str, int, float 등).
