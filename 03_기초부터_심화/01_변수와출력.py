# ---------- 따라 칠 코드 (1): 변수 만들고 출력 ----------
name = "김철수"
age = 25
height = 175.3

print(name)
print(age)
print(height)

name = "kkk"
age = "333"
height = "1999.99"

print(name)
print(age)
print(height)
print(name, "+", age, "+", height)
# ---------- 따라 칠 코드 (2): f-string ----------
# 중괄호 {} 안에 변수 이름을 넣으면, 그 자리에 값이 들어간다.
print(f"이름: {name}, 나이: {age}세, 키: {height}cm")
print(f"이름: {name}, 나이:{age}, 키:{height}cm")

# ---------- 따라 칠 코드 (3): 연산 결과도 변수에 담기 ----------
a = 10
b = 3
합 = a + b
몫 = a // b   # 나눗셈에서 소수 버리고 정수만
나머지 = a % b

a = 11
b = 2
합 = a + b
몫 = a // b
나머지 = a % b

print(f"합: {합}, 몫: {몫}, 나머지 : {나머지}")

print(f"합: {합}, 몫: {몫}, 나머지: {나머지}")

