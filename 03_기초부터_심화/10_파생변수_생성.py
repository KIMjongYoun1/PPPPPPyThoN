# ============================================================
# 10. 파생변수 생성 (빅분기·실무)
# ============================================================
#
# [파생변수란]
# 이미 있는 컬럼(변수)을 가지고, "새로운 변수"를 하나 더 만드는 것이다.
# 원본 그대로 쓰는 게 아니라, 조합·구간 나누기·계산으로 "의미 있는 정보"를 뽑아서 열을 추가하는 것.
#
# [왜 만드나]
# - 모델이 "숫자 하나"보다 "비율·구간·등급"이 더 잘 맞을 수 있다. 예: 나이 35보다 "30대"가 구분에 도움.
# - 해석이 쉬워진다. "지출비율이 높다"가 "지출 2000, 소득 5000"보다 말하기 좋다.
# - 변수 개수를 줄이거나, 노이즈를 줄이면서 정보를 담을 수 있다.
#
# [자주 쓰는 종류]
# 1) 비율: 소득 대비 지출 = 지출/소득, 이익률 = (매출-비용)/매출
# 2) 차이/합: 저축 = 소득 - 지출, 가입일수 = 오늘 - 가입일
# 3) 구간화(구간 나누기): 나이 → 연령대(청년/중년/장년), 점수 → 등급(상/중/하)
# 4) 조건 분류: 점수 >= 70 → 합격, 아니면 불합격
#
# [어떻게 만드나]
# - 비율·차이: 그냥 연산해서 새 열에 넣기. df["지출비율"] = df["지출"] / df["소득"] * 100
# - 구간 나누기(구간 경계 직접 지정): pd.cut(값, bins=[0,30,45,100], labels=["청년","중년","장년"])
# - 구간 나누기(구간마다 개수 비슷하게): pd.qcut(값, q=3, labels=["하","중","상"])
# - 조건으로 두 갈래: np.where(조건, 참일 때 값, 거짓일 때 값)
#
# [용어]
# - pd.cut: 구간 경계를 사람이 정함. 예: 0~30 청년, 30~45 중년.
# - pd.qcut: 구간 개수만 정하면, 데이터 개수가 비슷하게 나뉨. 균등 분할.
#
# [import — 각 모듈 역할]
# pandas (pd): DataFrame, cut(구간 나누기), qcut(균등 구간) 등.
# numpy (np): where(조건 분기), array 등.
# ============================================================

import pandas as pd
import numpy as np

# ---------- 따라 칠 코드 (1): 비율·차이 파생변수 ----------
df = pd.DataFrame({
    "소득": [3000, 5000, 4000],
    "지출": [1500, 2000, 2400],
})
df["지출비율"] = (df["지출"] / df["소득"] * 100).round(1)
df["저축"] = df["소득"] - df["지출"]
print(df)

