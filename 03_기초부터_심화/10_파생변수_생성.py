# ---------- 따라 칠 코드 (2): pd.cut 구간화 ----------

import pandas as pd
import numpy as np
ages = [22, 35, 48, 19, 55]
df2 = pd.DataFrame({"나이": ages})
df2["연령대"] = pd.cut(
    df2["나이"],
    bins=[0, 30, 45, 100],
    labels=["청년", "중년", "장년"]
)
print(df2)

