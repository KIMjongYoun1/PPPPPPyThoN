import pandas as pd



# ---------- 따라 칠 코드 (1): DataFrame 만들기 ----------
df = pd.DataFrame({
    "이름": ["철수", "영희", "민수", "지연"],
    "반":   ["A", "A", "B", "B"],
    "점수": [80, 90, 70, 85],
})
print(df)

# df = pd.DataFrame({
#     "이름" : ["철수","영희","민수","지연"],
#     "반" : ["a","A","b", "c"],
#     "점수" : [80,90,75,85]

# })

# ---------- 따라 칠 코드 (2): 열 선택, 조건 필터 ----------
print(df["점수"])
print(df[df["반"] == "c"])

# ---------- 따라 칠 코드 (3): groupby + 집계 ----------
# 반별로 묶어서 점수의 평균
by_class = df.groupby("반" )["점수"].mean()
print(by_class)

# by_class2 = df.groupby("반")["점수"].mean()
# print(by_class2)

# 반별로 여러 집계 한 번에
by_class2 = df.groupby("반").agg(평균=("점수", "mean"), 최대=("점수", "max"), 최소=("점수", "min"))
print(by_class2)

by_class3 = df.groupby("반").agg(평균 = ("점수", "mean"), 최대 = ("점수", "max"), 최소 = ("점수", "min"))
print(by_class3)

# ---------- 따라 칠 코드 (4): 구간 나누기 — pd.cut ----------
ages = [22, 35, 48, 19, 55, 31]
# 구간: 0~25, 25~40, 40~60
bins = [0, 25, 40, 60]
labels = ["청년", "중년", "장년"]
age_group = pd.cut(ages, bins=bins, labels=labels)
print(age_group)


