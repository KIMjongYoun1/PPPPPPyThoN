# groupby(열이름) 하면 그 열 값별로 묶이고, .agg() 로 평균·합계 같은 집계를 한 번에 해.
# 구간 나누기는 연속형 숫자를 "몇 점대", "몇 세대"처럼 구간으로 바꿀 때 써. pd.cut(값들, 구간경계) 쓰면 돼.
