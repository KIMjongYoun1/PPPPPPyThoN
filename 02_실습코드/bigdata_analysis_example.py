# 3-1. 독립표본 t검정
# -----------------------------------------------------------------------------
# [문제] 시험: "두 집단의 평균에 차이가 있는지 검정하시오." (예: 직업별 소득)
# [용어] t검정 = 두 집단의 평균 차이가 우연인지, 실제로 차이가 있는지 통계적으로 판단하는 방법.
# [용어] 독립표본 = 서로 다른 사람/대상으로 이루어진 두 그룹. (예: 직장인 그룹 vs 자영업 그룹)
# [용어] p-value = "우연히 이만큼 차이가 날 확률". 작을수록 "우연이 아니다 = 차이가 있다"고 봄.
# [용어] 유의수준 0.05 = 5%. p-value가 0.05 미만이면 "차이가 있다"고 결론. 0.05 이상이면 "차이 없다".

# [원리] 직업이 '직장인'인 행만 필터 후 '소득' 컬럼만 추출 → 직장인 소득 벡터.
group1 = df_encoded[df_encoded['직업'] == '직장인']['소득']
# [원리] 직업이 '자영업'인 행의 소득 벡터. 두 집단 비교 대상.
group2 = df_encoded[df_encoded['직업'] == '자영업']['소득']

# [원리] ttest_ind: 두 독립 표본의 평균 차이 검정. 반환: t통계량, p-value.
t_stat, p_value = stats.ttest_ind(group1, group2)
print("\n[3-1] 독립표본 t검정 (직장인 vs 자영업 소득)")
print(f"  t통계량: {t_stat:.4f}, p-value: {p_value:.4f}")
# [원리] p < 0.05이면 '차이가 있다', p >= 0.05이면 '차이 없다'고 결론.
if p_value < 0.05:
    print("  결론: 유의수준 5%에서 소득 평균에 차이가 있다.")
else:
    print("  결론: 유의수준 5%에서 소득 평균에 유의한 차이가 없다.")

# -----------------------------------------------------------------------------
# 3-2. 카이제곱 검정 (범주형 vs 범주형)
# -----------------------------------------------------------------------------
# [문제] 시험: "두 범주형 변수가 독립인지(관련 있는지) 검정하시오."
# [용어] 카이제곱 검정 = 두 범주형 변수가 서로 관련이 있는지(독립이 아닌지) 판단하는 검정.
# [용어] 독립 = 한 변수의 값이 다른 변수와 무관. 관련 있다 = 한 변수에 따라 다른 변수 분포가 달라짐.
# [용어] 분할표(교차표) = 두 범주형 변수를 행·열로 놓고, 조합별 개수를 적어 넣은 표.

# [원리] crosstab(행변수, 열변수): 두 범주형 변수의 조합별 빈도표(분할표) 만듦.
cross_tab = pd.crosstab(df_encoded['신용등급'], df_encoded['대출승인'])
# [원리] chi2_contingency(분할표): 카이제곱 통계량, p-value, 자유도, 기대도수 반환.
chi2, p_chi, dof, expected = stats.chi2_contingency(cross_tab)

print("\n[3-2] 카이제곱 독립성 검정 (신용등급 vs 대출승인)")
print(f"  카이제곱 통계량: {chi2:.4f}, p-value: {p_chi:.4f}")
# [원리] p < 0.05면 '독립이 아니다' = 두 변수 간 연관이 있다. p >= 0.05면 '독립이다' = 연관 없음.
if p_chi < 0.05:
    print("  결론: 신용등급과 대출승인 여부는 독립이 아니다 (관련 있음).")
else:
    print("  결론: 신용등급과 대출승인 여부는 독립이다.")

